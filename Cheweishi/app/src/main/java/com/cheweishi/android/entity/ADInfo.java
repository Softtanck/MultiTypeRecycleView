package com.cheweishi.android.entity;

/**
 * 广告对象
 * 
 * @author mingdasen
 * 
 */
public class ADInfo {
	private String avd_id;
	private String slide_sort;
	private String adv_content;
	private String adv_title;
	private String ap_id;
	private int width;
	private int height;
	private int defaultImg;

	public String getAvd_id() {
		return avd_id;
	}

	public void setAvd_id(String avd_id) {
		this.avd_id = avd_id;
	}

	public String getSlide_sort() {
		return slide_sort;
	}

	public void setSlide_sort(String slide_sort) {
		this.slide_sort = slide_sort;
	}

	public String getAdv_content() {
		return adv_content;
	}

	public void setAdv_content(String adv_content) {
		this.adv_content = adv_content;
	}

	public String getAdv_title() {
		return adv_title;
	}

	public void setAdv_title(String adv_title) {
		this.adv_title = adv_title;
	}

	public String getAp_id() {
		return ap_id;
	}

	public void setAp_id(String ap_id) {
		this.ap_id = ap_id;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public void setHeight(int height) {
		this.height = height;
	}

	public int getDefaultImg() {
		return defaultImg;
	}

	public void setDefaultImg(int defaultImg) {
		this.defaultImg = defaultImg;
	}
}
